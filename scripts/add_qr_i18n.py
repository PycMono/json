def append_keys(lang, new_keys):
    path = os.path.join(BASE, lang, "qr.json")
    with open(path, "r", encoding="utf-8") as f:
        data = json.load(f)

    added = 0
    for k, v in new_keys.items():
        if k not in data:
            data[k] = v
            added += 1

    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write("\n")

    print(f"Appended {added} keys to {path}")

